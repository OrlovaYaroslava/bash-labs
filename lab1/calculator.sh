# Проверяем, что передано 3 аргумента
if [ $# -ne 3 ]; then
    echo "Ошибка: требуется 3 аргумента: число1, оператор, число2."
    echo "Пример: $0 5 + 3"
    exit 1
fi

# Сохраняем аргументы
num1=$1
operator=$2
num2=$3

# Выполняем операцию в зависимости от оператора
case $operator in
    "+")
        result=$((num1 + num2))
        ;;
    "-")
        result=$((num1 - num2))
        ;;
    "*")
        result=$((num1 * num2))
        ;;
    "/")
        # Проверяем деление на ноль
        if [ $num2 -eq 0 ]; then
            echo "Ошибка: деление на ноль."
            exit 1
        fi
        result=$((num1 / num2))
        ;;
    *)
        echo "Ошибка: неподдерживаемый оператор. Используйте +, -, *, /"
        exit 1
        ;;
esac

# Выводим результат
echo "Результат: $result"