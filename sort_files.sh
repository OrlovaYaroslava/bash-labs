# Проверяем, передан ли аргумент
if [ $# -eq 0 ]; then
    echo "Ошибка: не указано имя директории."
    exit 1
fi

# Сохраняем имя директории
directory=$1

# Проверяем, существует ли директория
if [ ! -d "$directory" ]; then
    echo "Ошибка: директория '$directory' не существует."
    exit 1
fi

# Выводим список файлов, отсортированных по дате (новые сверху)
echo "Файлы в директории '$directory', отсортированные по дате (новые сверху):"
ls -lt "$directory"